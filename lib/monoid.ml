open Type

module type Monoid = sig 
  type t 
  val mempty : t
  val mconcat : t -> t -> t
end

module MonoidList (T : Type) : Monoid with type t = T.t list = struct
  type t = T.t list
  let mempty = [] 
  let mconcat = (@)
end 